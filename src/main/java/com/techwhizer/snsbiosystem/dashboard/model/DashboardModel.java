package com.techwhizer.snsbiosystem.dashboard.model;

public class DashboardModel {
    private Integer totalKits;
    private Integer totalUsers;
    private Integer totalSterilizers;

    public Integer getTotalKits() {
        return totalKits;
    }

    public void setTotalKits(Integer totalKits) {
        this.totalKits = totalKits;
    }

    public Integer getTotalUsers() {
        return totalUsers;
    }

    public void setTotalUsers(Integer totalUsers) {
        this.totalUsers = totalUsers;
    }

    public Integer getTotalSterilizers() {
        return totalSterilizers;
    }

    public void setTotalSterilizers(Integer totalSterilizers) {
        this.totalSterilizers = totalSterilizers;
    }
}
